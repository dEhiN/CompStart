# File to store the description of the CompStart program for use when the user selects option 1 from the main menu

CS_DESCRIPTION = """
What is CompStart?

CompStart is a computer startup tool designed to make your life easier.
--
What does that mean?

Have you ever wished to be able to log into your laptop at work and have all of your programs automatically start up? Or, maybe you are working on some personal project at home and don't want to be reopening the file or program you're using every time you turn on your computer.

With CompStart, you can have:

- any program automatically open when you log in
- your favourite browser open to any website (or sites) you desire
- any file open using the appropriate program
--
How does it work?

CompStart uses a file to store the programs, sites, and files to automatically open. This file is referred to throughout as the startup file. Each item or entry is referred to as a startup item. Startup items can have arguments.

This text-based tool will let you modify that startup file. For example, if you want to have Microsoft Word open to a document that you are currently working on, you could use this tool to add a new startup item. You would set up the new startup item to open Microsoft Word. You would also pass in as an argument the document you want to open. As another example, if you want your favourite browser to open and show a specific site that you visit often, or are using for work, you would add a new startup item. This time, the startup item would be set to open your favourite browser, and the argument given would be the site you want opened.
--
Is there a graphical way to modify the startup file?

At this time, there is not. The only other way to modify the startup file is to do so manually, and that can be complicated. This text-based tool was created to allow for a an easier way to modify the startup file. It's not as easy as a graphical tool, but it is easier than manual modification.
--
How do I use this tool?

As you saw when you first started this program, or tool, a menu is displayed asking you to make a choice. Choosing the first option brought you here, where you are now reading this description. Since the tool is text-based, it uses menus and prompts throughout, just like that first menu.
--
What if I need help with this tool?

If you need help with this tool, please go to https://github.com/dEhiN/CompStart/issues and click the New issue button. Try to give us as much information as you can about what you were trying to do and what happened.
--
What if I encounter an error?

If you encounter an error, please create a new issue using the same steps above. There should be one or more error messages that are printed out. We will need that information, so either copy the messages or take a screenshot of it. Again, try to give us as much information as you can about what you were trying to do.
"""
